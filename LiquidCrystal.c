int divider = 0, noteDuration = 0;

void playTheTone(int melody[], int sizeOfMelody, int tempo) {
  // change this to make the song slower or faster
  //int tempo = 180;

  // sizeof gives the number of bytes, each int value is composed of two bytes (16 bits)
  // there are two values per note (pitch and duration), so for each note there are four bytes
  //int notes = sizeof(melody) / sizeof(melody[0]) / 2;
  int notes = sizeOfMelody / 2;

  // this calculates the duration of a whole note in ms
  int wholenote = (60000 * 4) / tempo;

  // iterate over the notes of the melody.
  // Remember, the array is twice the number of notes (notes + durations)
  for (int thisNote = 0; thisNote < notes * 2; thisNote = thisNote + 2) {

    // calculates the duration of each note
    divider = melody[thisNote + 1];
    if (divider > 0) {
      // regular note, just proceed
      noteDuration = (wholenote) / divider;
    } else if (divider < 0) {
      // dotted notes are represented with negative durations!!
      noteDuration = (wholenote) / abs(divider);
      noteDuration *= 1.5; // increases the duration in half for dotted notes
    }

    // we only play the note for 90% of the duration, leaving 10% as a pause
    tone(buzzer, melody[thisNote], noteDuration * 0.9);

    // Wait for the specief duration before playing the next note.
    delay(noteDuration);

    // stop the waveform generation before the next note.
    noTone(buzzer);
  }
}

void clearLCDLine(int line){
 for(int n = 0; n < 16; n++) {  // 20 indicates symbols in line. For 2x16 LCD write - 16
   lcd.setCursor(n,line);
   lcd.print(" ");
 }
}

void setup() {
  // set up the LCD's number of columns and rows:
  lcd.begin(16, 2);
  // Print a message to the LCD.
  lcd.print("Hello viGlobal!");

  pinMode(buzzer, OUTPUT);
}

char getNormalTime(unsigned long seconds){
  char buffer [10];
  uint8_t hours, minutes;
  minutes = seconds / 60;
  hours = minutes / 60;
  sprintf (buffer, "%02u:%02u:%02u", hours, minutes, seconds%60);
  return buffer;
}

void loop() {
  // set the cursor to column 0, line 1
  // (note: line 1 is the second row, since counting begins with 0):
  lcd.setCursor(0, 1);
  unsigned long currentMillis = millis();
  // print the number of seconds since rollover:
  lcd.print((currentMillis - previousMillis)/1000);
  lcd.setCursor(8, 1);

  char buffer [10];
  uint8_t hours, minutes, seconds;
  seconds = currentMillis/1000;
  minutes = seconds / 60;
  hours = minutes / 60;
  sprintf (buffer, "%02u:%02u:%02u", hours, minutes, seconds%60);

  lcd.write(buffer);
  
  if ((unsigned long)(currentMillis - previousMillis)/1000 >= resetTime) {
    previousMillis = currentMillis;
    lcd.setCursor(0, 1);
    lcd.print("It's Tea Time!");
    playTheTone(melody_Nokia, ELEMENTSIZE(melody_Nokia), 180);
    //playTheTone(melody_HappyBirthDay, ELEMENTSIZE(melody_HappyBirthDay), 140);
    clearLCDLine(1);
  }
}
